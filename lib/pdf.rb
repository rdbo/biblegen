module Bible
  require 'hexapdf'

  class Book
    def to_pdf!(doc)
    end
  end

  class Bible
    def to_pdf
      composer = HexaPDF::Composer.new
      composer.text("Public Domain Catholic Bible")
      composer.text("Texts by https://www.sacredbible.org")
      composer.text("PDF generated by Rdbo")
      composer.text(
        "The Holy Bible",
        text_valign: :center,
        text_align: :center,
        font_size: 48,
        font_bold: true
      )
      composer
    end
  end
end
